public class solid_rectangle {
    public static void main(String[] args){
        int c = 5;
        int r = 4;
        for(int i = 0 ; i < r; i++){
            for(int j = 1; j < c; j++){
                System.out.print("*"+" ");
            }
            System.out.println("*");
        }
        System.out.println(" ");
    }
}
